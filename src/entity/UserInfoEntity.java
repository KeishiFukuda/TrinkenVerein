package entity;

public class UserInfoEntity {
	String UserId;
	String Password;
	String Age;

	public String getUserId() {
		return UserId;
	}
	public void setUserId(String userId) {
		UserId = userId;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public String getAge() {
		return Age;
	}
	public void setAge(String age) {
		Age = age;
	}



}
